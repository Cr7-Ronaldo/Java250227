package ex03;

public class SonyTv extends Tv{
	
	void powerOn() {
		System.out.println("SonyTv");
		System.out.println("전원 on");
	}
	
		
	void powerOff() {
		System.out.println("SonyTv");
		System.out.println("전원 off");
	}
		   
	void volumeUp() {
		System.out.println("SonyTv");
		System.out.println("볼륨 up");
	}
		
	void volumeDown() {
		System.out.println("SonyTv");
		System.out.println("볼륨 down");
	}
}